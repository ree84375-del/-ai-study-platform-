def get_text(key, lang='zh', **kwargs):
    if lang not in TRANSLATIONS:
        lang = 'zh'
    text = TRANSLATIONS[lang].get(key, TRANSLATIONS['zh'].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text
